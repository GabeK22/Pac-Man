def square(x, y):
    """Draw square using path at (x, y)."""
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()


def offset(point):
    """Return offset of point in tiles."""
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index


def valid(point):
    """Return True if point is valid in tiles."""
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0


def world():
    """Draw world using path."""
    bgcolor('OliveDrab1')
    path.color('RoyalBlue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(4, 'white')


def move():
    """Move pacmans and all ghosts."""
    writer.undo()
    writer.write(state['score'], font =('Courier',20,'bold'))

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(25, 'gold1')
    
    
    if valid(pacman1 + aim1):
        pacman1.move(aim1)

    index = offset(pacman1)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman1.x + 10, pacman1.y + 10)
    dot(25, 'hotpink')
    
    for point, course in ghost1, ghost2, ghost3, ghost4:
        if valid(point + course):
            point.move(course)
        else:
            options = [
                vector(5, 0),
                vector(-5, 0),
                vector(0, 5),
                vector(0, -5),
            ]
            plan = choice(options)
            course.x = plan.x
            course.y = plan.y

        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'LightGoldenrod')
        dot(15.5, 'VioletRed1')
        dot(10, 'RoyalBlue')

    update()

    for point, course in ghost1, ghost2, ghost3, ghost4:
        if abs(pacman - point) < 20 or abs(pacman1 - point) < 20:
            print ('You Died:(') 
            points=str(state)
            print ('Your Score:'+points.strip('{\'score\':}'))
            exit()

    ontimer(move, 100)


def change(x, y):
    """Change pacman aim if valid."""
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y
def change1(x1, y1):
    """Change pacman aim if valid."""
    if valid(pacman1 + vector(x1, y1)):
        aim1.x = x1
        aim1.y = y1

